package Model;

public class Pair {
    
    private int xPos, xFinal, length;
 
    public int getxPos() {
        return xPos;
    }
 
    public int getxFinal() {
        return xFinal;
    }
 
    public int getLength() {
        return length;
    }

    public void setxPos(int xPos) {
        this.xPos = xPos;
    }

    public void setxFinal(int xFinal) {
        this.xFinal = xFinal;
    }
 
    public void setLength(int length) {
        this.length = length;
    } 
}
